numbers = [3, 1, 4, 1, 5, 9, 2]

print(numbers[0]) # Answer: The value for the expression is 3.
print(numbers[-1]) # Answer: The value for the expression is 2.
print(numbers[3]) # Answer: The value for the expression is 4.
print(numbers[:-1]) # Answer: The values for the expression is [3, 1, 4, 1, 5, 9].
print(numbers[3:4]) # Answer: The value for the expression is 1.
print(5 in numbers) # Answer: The value for the expression is [3, 1, 4, 1, 5].
print(7 in numbers) # Answer: The value for the expression is [3, 1, 4, 1, 5, 9, 2].
print("3" in numbers) # Answer: The value for the expression is True/3.
result = numbers + [6, 5, 3]
print(result) # Answer: The value for the expression is [3, 1, 4, 1, 5, 9, 2, 6, 5, 3].



